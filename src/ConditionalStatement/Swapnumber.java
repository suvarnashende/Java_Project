package ConditionalStatement;

public class Swapnumber {

	public static void main(String[] args) {
		int x = 500;
		int y = 1000;
		System.out.println("before swap x = " + x +" and y = " + y +"");
		int eng = x;
		x=y;
		y = eng;
		System.out.println("after swap x = " + x +" and y = " + y +"");
	}

}
